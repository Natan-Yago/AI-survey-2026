/** Resolve a numeric average (1–5) to its maturity level. */
export function levelForScore(avg: number): MaturityLevel {
  for (const level of MATURITY_LEVELS) {
    if (avg <= level.range[1]) return level;
  }
  return MATURITY_LEVELS[MATURITY_LEVELS.length - 1];
}

